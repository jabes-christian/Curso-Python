b = input('Digite um algo = ')
print('O tipo primitivo desse valor é {}'.format(type(b)))
print('Só tem espaços?{}'.format(b.isspace()))
print('É um número?{}'.format(b.isnumeric()))
print('É alfabetico?{}'.format(b.isalpha()))
print('É alfanumérico?{}'.format(b.isalnum()))
print('Está em maiúscula?{}'.format(b.isupper()))
print('Está em minúscula?{}'.format(b.islower()))
print('Está capitalizada?{}'.format(b.istitle()))

